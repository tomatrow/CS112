// This class describes a Wolf that inherits from the Animal class

public class Wolf extends Animal
{
	// construct an animal
	public Wolf()
	{	
	}
	
	// method to make noise
	public void makeNoise()
	{
		System.out.println("I am a Wolf - hear me howl!\n");
	}
	
	// method to roam
	public void roam()
	{
		System.out.println("I like to roam amongst the forest trees.\n");
	}		
}
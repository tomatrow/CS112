// This class describes a Lion that inherits from the Animal class

public class Lion extends Animal
{
	// construct an animal
	public Lion()
	{	
	}
	
	// method to make noise
	public void makeNoise()
	{
		System.out.println("I am a Lion - hear me roar!\n");
	}
	
	// method to roam
	public void roam()
	{
		System.out.println("I like to roam the African grasslands.\n");
	}		
}
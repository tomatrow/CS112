// This class describes a Dog that inherits from the Animal class

public class Dog extends Animal
{
	// construct an animal
	public Dog()
	{	
	}
	
	// method to make noise
	public void makeNoise()
	{
		System.out.println("I am a Dog - hear me bark!\n");
	}
	
	// method to roam
	public void roam()
	{
		System.out.println("I roam, but I can always hear the biscuit jar.\n");
	}	
}
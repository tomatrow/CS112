// This class describes a KittyCat that inherits from the Animal class

public class KittyCat extends Animal
{
	// construct an animal
	public KittyCat()
	{	
	}
	
	// method to make noise
	public void makeNoise()
	{
		System.out.println("I am a Kitten - hear me meow.\n");
	}
	
	// method to roam
	public void roam()
	{
		System.out.println("I like to roam at night.\n");
	}		
}